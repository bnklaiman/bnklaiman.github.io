"use strict"; // Do NOT remove this directive!

PS.init = function( system, options ) {
	// Establish grid dimensions

	PS.gridSize( 8, 4 );

	// Set background color to a light blue

	PS.gridColor( 0x43C052 );

	// Change status line color and text

	PS.statusColor( PS.COLOR_BLUE );
	PS.statusText( "Click each bead multiple times." )

	// Preload click sound

	PS.audioLoad( "fx_click" );
};

PS.touch = function( x, y, data, options ) {
	// Toggle color of touched bead from white to black to orange and back again
	// NOTE: The default value of a bead's [data] is 0, which happens to be equal to PS.COLOR_BLACK

	PS.color( x, y, data ); // set color to current value of data

	// Decide what the next color should be.
	// If the current value was black, change it to orange.
	// If the current value was orange, change it to white.
	// If the current value was white, change it to black

	// NOTE: This is not the most succinct way to code this functionality.
	// It's written this way for clarity.

	let next; // variable to save next color

	if ( data === PS.COLOR_BLACK ) {
		next = PS.COLOR_ORANGE;
	}
	if ( data === PS.COLOR_ORANGE ){
		next = PS.COLOR_WHITE;
	}
	if ( data === PS.COLOR_WHITE ) {
		next = PS.COLOR_BLACK;
	}

	// Remember the newly-changed color by storing it in the bead's data.

	PS.data( x, y, next );

	// Play click sound.

	PS.audioPlay( "fx_chirp1" )

};

